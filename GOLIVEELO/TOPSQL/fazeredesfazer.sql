AND C.DS_VERSAO IS NOT NULL
AND C.CD_TIPO_AGENDAMENTO = 25 AND C.CD_STATUS_REPLAN = 32 AND C.QT_AGENDADA_CONFIRMADA = 0
AND NOT(C.CD_STATUS_CEL_FINAL = 59)
AND EXISTS (SELECT 1 FROM VND.PEDIDO P
WHERE P.NU_ORDEM_VENDA = C.NU_ORDEM_VENDA 
