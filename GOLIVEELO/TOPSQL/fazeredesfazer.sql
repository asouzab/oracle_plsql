|| ' WHERE CD_ELO_CARTEIRA = ' || TO_CHAR(C.CD_ELO_CARTEIRA) 
|| ' AND NU_ORDEM_VENDA = ''' || C.NU_ORDEM_VENDA || '''' ||  
';' || '--' || C.DS_VERSAO

SELECT C.*

FROM VND.ELO_CARTEIRA C
WHERE 1=1 
--C.CD_ELO_AGENDAMENTO = 72
