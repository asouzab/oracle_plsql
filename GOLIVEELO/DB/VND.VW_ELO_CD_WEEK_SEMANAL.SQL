DROP MATERIALIZED VIEW VND.VW_ELO_CD_WEEK_SEMANAL;
CREATE MATERIALIZED VIEW VND.VW_ELO_CD_WEEK_SEMANAL 
    (CD_WEEK, DT_WEEK_START_DAY)
TABLESPACE WEB_DATA
PCTUSED    0
PCTFREE    10
INITRANS   2
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOCACHE
NOLOGGING
NOCOMPRESS
BUILD IMMEDIATE
REFRESH COMPLETE
START WITH TO_DATE('23-05-2018 01:00:13','dd-mm-yyyy hh24:mi:ss')
NEXT SYSDATE+0.0033
WITH PRIMARY KEY
AS 
SELECT 
AGE.CD_WEEK, 
TRUNC(AGE.dt_week_start, 'day') DT_WEEK_START_DAY
FROM VND.ELO_AGENDAMENTO AGE
WHERE
AGE.IC_ATIVO = 'S'
GROUP BY 
AGE.CD_WEEK, 
TRUNC(AGE.dt_week_start, 'day')
;

COMMENT ON MATERIALIZED VIEW VND.VW_ELO_CD_WEEK_SEMANAL IS 'snapshot table for snapshot VND.VW_ELO_CD_WEEK_SEMANAL';

GRANT SELECT ON VND.VW_ELO_CD_WEEK_SEMANAL TO CPT;

GRANT SELECT ON VND.VW_ELO_CD_WEEK_SEMANAL TO VND_SEC;

GRANT SELECT ON VND.VW_ELO_CD_WEEK_SEMANAL TO VND;